import numpy
import numpy as np


def pprint(msg, arr: np):
    print(f'[[[ {msg} ]]]')
    (n, m) = arr.shape
    for i in range(0, n):
        line = ''
        for j in range(0, m):
            line += "{0:.3f}\t".format(arr[i, j])
            if j == n - 1:
                line += "| "
        print(line)
    print("")


# 가우스-조단 소거법을 수행하는 함수
def gauss(arr: numpy):
    (n, m) = arr.shape

    for i in range(0, n):
        # i번째 열에서 절댓값이 최대인 성분의 행 선택
        maxEl = abs(arr[i, i])
        maxRow = i
        for k in range(i + 1, n):
            if abs(arr[k, i]) > maxEl:
                maxEl = abs(arr[k, i])
                maxRow = k

        # 현재 i번째 행과 절댓값이 최대인 행 maxRow의 교환
        for k in range(i, m):
            arr[maxRow, k], arr[i, k] = arr[i, k], arr[maxRow, k]

        pprint(f'{str(i + 1)}행과 {str(maxRow + 1)}행을 교환: R{str(i + 1)} <-> R{str(maxRow + 1)}', arr)

        # 피벗을 1로 만들기
        pivot = arr[i, i]
        for k in range(i, m):
            arr[i, k] = arr[i, k] / pivot
        pprint(
            f'{str(i + 1)}행 {str(i + 1)}열 피벗을 1로 변환: R{str(i + 1)} <- ( 1 / {"{0:.3f}".format(pivot)} * R{str(i + 1)}',
            arr)

        # 현재 i번째 열의 i 번째 행을 제외한 모든 성분을 0으로 만들기
        for k in range(0, n):
            if k != i:
                c = arr[k, i] / arr[i, i]
                for j in range(i, m):
                    if i == j:
                        arr[k, j] = 0
                    else:
                        arr[k, j] = arr[k, j] - c * arr[i, j]
                    pprint(
                        f'{str(k + 1)}행 {str(i + 1)}열 성분을 0으로 변환: R{str(k + 1)} '
                        f'<- {"{0:.3f}".format(-c)} * R{str(i + 1)} + R{str(k + 1)}',
                        arr)
    # Ax = b의 해 반환
    x = np.zeros(n)
    for i in range(0, n):
        x[i] = arr[i, n]
    return x


